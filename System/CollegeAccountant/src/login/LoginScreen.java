    private void buttonSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSubmitActionPerformed
        try
        {
            System.out.println("Path came: "+ProjResouces.LOGIN_LOG_PATH);
            FileOutputStream f = new FileOutputStream(ProjResouces.LOGIN_LOG_PATH);
            System.setErr(new PrintStream(f));
        }
        catch(Exception e){
            System.out.println(e.getMessage());
        }
        ArrayList<String> loginInfo = new ArrayList<String>();
        System.err.println( new Date().toString()+":LoginScreen: "+"Size of LoginInfo: "+ (LoginUtils.getLoginInfo(textUserName.getText().trim())).size());
        if((LoginUtils.getLoginInfo(textUserName.getText().trim())).size()>0)
        {
            loginInfo = LoginUtils.getLoginInfo(textUserName.getText().trim());
            userName = loginInfo.get(0);
            System.err.println( new Date().toString()+":LoginScreen: "+"userName: "+userName);
            password = loginInfo.get(1);
            System.err.println( new Date().toString()+":LoginScreen: "+"password: "+password);
            accountFlag = Integer.parseInt(loginInfo.get(2));
            System.err.println( new Date().toString()+":LoginScreen: "+"accountFlag: "+accountFlag);
        }
        else
        {
            labelWarning.setText("Sorry, account cannot be found.");
        }
        if(accountFlag == 0)
        {
            if(textUserName.getText().trim().length()>0 && textPassword.getPassword().length>0 && textCaptchaAns.getText().trim().length()>0)
            {
                if(textUserName.getText().equalsIgnoreCase(userName) && textPassword.getText().equalsIgnoreCase(password))
                {
                    try
                    {  
                        if(textCaptchaAns.getText().trim().length()>0)
                        {
                            intCaptchaAns = Integer.parseInt(textCaptchaAns.getText());
                            if((intCaptchaAns!=captchaVar))
                            {
                                this.callCaptcha(2);
                            }
                            else
                            {
                                mainscreen.FrameMainMenu mainScreen = new mainscreen.FrameMainMenu();
                                mainScreen.setSize(600, 400);
                                mainScreen.setVisible(true);
                                this.dispose();
                            }
                        }
                    }
                    catch(NumberFormatException e)
                    {
                       this.callCaptcha(4);
                    }

                }
                else
                {
                    if(count!=1)
                    {
                        count--;
                        labelWarning.setText("You have Entered wrong password. You have "+count+" tries left.");
                        this.callCaptcha(3);
                    }
                    else
                    {
                        labelWarning.setText("Your account has been De-Activated.");

                    }
                }
            }
            else
            {
                if((textUserName.getText().trim().length()==0) && (textPassword.getPassword().length==0) && (textCaptchaAns.getText().trim().length()==0))
                {
                    textUserName.setBackground(Color.red);
                    textPassword.setBackground(Color.red);
                    textCaptchaAns.setBackground(Color.red);
                    labelWarning.setText("Please fill the details below.");
                }
                else if((textUserName.getText().trim().length()==0))
                {
                    textUserName.setBackground(Color.white);
                    textPassword.setBackground(Color.white);
                    textCaptchaAns.setBackground(Color.white);
                    labelWarning.setText("Please enter User Name.");
                }
                else if((textPassword.getPassword().length==0))
                {
                    textUserName.setBackground(Color.white);
                    textPassword.setBackground(Color.red);
                    textCaptchaAns.setBackground(Color.white);
                    labelWarning.setText("Please enter Password.");
                }
                else if((textCaptchaAns.getText().trim().length()==0))
                {
                    textUserName.setBackground(Color.white);
                    textPassword.setBackground(Color.white);
                    textCaptchaAns.setBackground(Color.red);
                    labelWarning.setText("Please solve the puzzle.");
                }
            }
        }
        else
        {
            labelWarning.setText("Your account is already Desabled.");
        }
    }//GEN-LAST:event_buttonSubmitActionPerformed

