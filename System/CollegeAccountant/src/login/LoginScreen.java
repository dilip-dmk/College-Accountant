    public void callCaptcha(int intType)
    {
        if(intType==1)
        {
            labelWarning.setText("Please fill the details below.");
            captchaNum1 = CaptchaEngine.randIntOne(1, 99);
            captchaNum2 = CaptchaEngine.randIntTwo(1, 99);
            captchaVar = captchaNum1+captchaNum2;
            labelCaptcha.setText(captchaNum1+" + "+captchaNum2+" = ");
        }
        if(intType==2)
        {
            labelWarning.setText("Please solve the puzzle again.");
            textUserName.setBackground(Color.white);
            textPassword.setBackground(Color.white);
            textCaptchaAns.setBackground(Color.red);
            textCaptchaAns.setText("");
            captchaNum1 = CaptchaEngine.randIntOne(1, 99);
            captchaNum2 = CaptchaEngine.randIntTwo(1, 99);
            captchaVar = captchaNum1+captchaNum2;
            labelCaptcha.setText(captchaNum1+" + "+captchaNum2+" = ");
        }
        if(intType==3)
        {
            textUserName.setBackground(Color.white);
            textCaptchaAns.setBackground(Color.white);
            textPassword.setBackground(Color.red);
            textPassword.setText("");
            textCaptchaAns.setText("");
            captchaNum1 = CaptchaEngine.randIntOne(1, 99);
            captchaNum2 = CaptchaEngine.randIntTwo(1, 99);
            captchaVar = captchaNum1+captchaNum2;
            labelCaptcha.setText(captchaNum1+" + "+captchaNum2+" = ");
        }
        if(intType==4)
        {
            textUserName.setBackground(Color.white);
            textPassword.setBackground(Color.white);
            textCaptchaAns.setBackground(Color.red);
            textCaptchaAns.setText("");
            labelWarning.setText("Please use number in Puzzle answer.");
            captchaNum1 = CaptchaEngine.randIntOne(1, 100);
            captchaNum2 = CaptchaEngine.randIntTwo(1, 100);
            captchaVar = captchaNum1+captchaNum2;
            labelCaptcha.setText(captchaNum1+" + "+captchaNum2+" = ");
        }
    }
